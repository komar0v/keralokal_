public class OBJ_toko {
    private int id_toko;
    private String nama_toko, alamat_toko, kontak_toko, username_toko, password_toko;

    public int getId_toko() {
        return id_toko;
    }

    public void setId_toko(int id_toko) {
        this.id_toko = id_toko;
    }

    public String getNama_toko() {
        return nama_toko;
    }

    public void setNama_toko(String nama_toko) {
        this.nama_toko = nama_toko;
    }

    public String getAlamat_toko() {
        return alamat_toko;
    }

    public void setAlamat_toko(String alamat_toko) {
        this.alamat_toko = alamat_toko;
    }

    public String getKontak_toko() {
        return kontak_toko;
    }

    public void setKontak_toko(String kontak_toko) {
        this.kontak_toko = kontak_toko;
    }

    public String getUsername_toko() {
        return username_toko;
    }

    public void setUsername_toko(String username_toko) {
        this.username_toko = username_toko;
    }

    public String getPassword_toko() {
        return password_toko;
    }

    public void setPassword_toko(String password_toko) {
        this.password_toko = password_toko;
    }
}
